def load_json(path: str | Path) -> dict[str, Any]:
    path = Path(path)

    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def build_category_maps(coco: dict[str, Any]) -> tuple[dict[int, str], dict[str, int]]:
    id_to_name: dict[int, str] = {}

    for cat in coco.get("categories", []):
        cat_id = int(cat["id"])
        name = str(cat["name"])
        id_to_name[cat_id] = name

    name_to_id = {name: cat_id for cat_id, name in id_to_name.items()}
    return id_to_name, name_to_id


def group_annotations_by_image(coco: dict[str, Any]) -> dict[int, list[dict[str, Any]]]:
    anns_by_image: dict[int, list[dict[str, Any]]] = defaultdict(list)

    for ann in coco.get("annotations", []):
        image_id = int(ann["image_id"])
        anns_by_image[image_id].append(ann)

    return anns_by_image


# =============================================================================
# Prompt and target strategies
# =============================================================================

def class_list_text(class_names: Iterable[str]) -> str:
    names = list(class_names)

    if not names:
        return "objects"

    if len(names) == 1:
        return names[0]

    if len(names) == 2:
        return f"{names[0]} and {names[1]}"

    return ", ".join(names[:-1]) + f", and {names[-1]}"


def build_simple_prompt(class_names: list[str]) -> str:
    classes = class_list_text(class_names)

    return (
        f"Detect all {classes} instances. "
        "Return only a JSON list. "
        "Each item must have the format "
        '{"class":"<class_name>","box":[x1,y1,x2,y2]}. '
        "The box coordinates must be integers normalized to a 1000x1000 coordinate space."
    )


def build_prompt(
    *,
    cfg: CocoToCanonicalConfig,
    selected_class_names: list[str],
) -> str:
    if cfg.prompt_text is not None:
        return cfg.prompt_text

    if cfg.prompt_strategy == "simple_prompt":
        return build_simple_prompt(selected_class_names)

    raise ValueError(
        f"Unsupported prompt_strategy={cfg.prompt_strategy!r}. "
        f"Currently supported: {SUPPORTED_PROMPT_STRATEGIES}"
    )


def build_target_text_bbox_json(target_items: list[dict[str, Any]]) -> str:
    """
    Assistant target.

    Example:
        [{"class":"stator","box":[363,107,401,142]}]
    """
    return json.dumps(
        target_items,
        ensure_ascii=False,
        separators=(",", ":"),
    )


def build_target_text(
    *,
    cfg: CocoToCanonicalConfig,
    target_items: list[dict[str, Any]],
) -> str:
    if cfg.target_strategy == "bbox_json_norm1000":
        return build_target_text_bbox_json(target_items)

    raise ValueError(
        f"Unsupported target_strategy={cfg.target_strategy!r}. "
        f"Currently supported: {SUPPORTED_TARGET_STRATEGIES}"
    )


