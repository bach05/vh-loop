#!/usr/bin/env python3
"""
coco_to_canonical.py

Convert a COCO instance annotation JSON into a compact canonical VLM JSONL manifest.

Output format:

Line 1:
{"record_type":"dataset_info","schema_version":"vlmdojo.dataset.v1","info":{...}}

Lines 2+:
{"record_type":"sample", ... VLMSample-compatible fields ...}

Schema notes:
- The query image is stored in sample["images"]["query"].
- Message image references use image_id="query".
- COCO bbox annotations are converted into Annotation.bbox.
- One positive point is generated from each bbox center and stored in Annotation.pos_points.
- Negative points are not generated by this converter.
- Masks are not exported by this converter version.

Precedence:
    hardcoded defaults < YAML config < CLI overrides

Example:

python scripts/data/coco_to_canonical.py \
  --config configs/data/panizzolo_coco_to_canonical.yaml

python scripts/data/coco_to_canonical.py \
  --config configs/data/panizzolo_coco_to_canonical.yaml \
  --split val \
  --output-jsonl /tmp/panizzolo_val.vlm.jsonl
"""

from __future__ import annotations

import argparse
import json
from collections import defaultdict
from dataclasses import asdict, dataclass
from datetime import datetime
from pathlib import Path
from typing import Any, Iterable, List

import yaml
from tqdm import tqdm

from scripts.core.constants import (
    NORM_SIZE,
    SCHEMA_VERSION,
    SUPPORTED_TARGET_STRATEGIES,
    SUPPORTED_PROMPT_STRATEGIES,
)


# =============================================================================
# Config
# =============================================================================

@dataclass
class CocoToCanonicalConfig:
    coco_json: Path | None = None
    output_jsonl: Path | None = None

    dataset_id: str | None = None
    split: str | None = None
    description: str | None = None
    annotation_source: str = "human"

    prompt_strategy: str = "simple_prompt"
    target_strategy: str = "bbox_json_norm1000"

    # Optional custom prompt. If provided, overrides prompt_strategy text.
    prompt_text: str | None = None

    disable_pbar: bool = False

    drop_empty: bool = False
    include_categories: set[int] | None = None

    # Skip annotations whose bbox becomes invalid after clipping.
    skip_invalid_bboxes: bool = True

    # Store original COCO annotation metadata at sample level.
    include_instance_metadata: bool = True


def load_yaml_config(path: str | Path) -> Any:
    path = Path(path)

    with path.open("r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    # If the file is empty return an empty structure
    if data is None:
        return {}

    # Accept either a mapping (single config) or a sequence (list of configs)
    if isinstance(data, dict) or isinstance(data, list):
        return data

    raise TypeError(f"YAML config root must be a mapping or a list of mappings, got {type(data)}")



def normalize_include_categories(value: Any) -> set[int] | None:
    """
    Accepts:
    - null / None
    - [1, 2, 3]
    - "1,2,3"
    """
    if value is None or value == "":
        return None

    if isinstance(value, str):
        return {int(v.strip()) for v in value.split(",") if v.strip()}

    if isinstance(value, (list, tuple, set)):
        return {int(v) for v in value}

    raise TypeError(
        "include_categories must be null, a list of ints, or a comma-separated string"
    )


def build_config_from_yaml_and_cli(args: argparse.Namespace, yaml_item: dict[str, Any] | None = None) -> CocoToCanonicalConfig:
    """
    Build a CocoToCanonicalConfig by merging:
      - dataclass defaults
      - optional per-item YAML mapping (yaml_item)
      - explicit CLI overrides (args)
    If yaml_item is None we still honor CLI overrides and dataclass defaults.
    """
    cfg = CocoToCanonicalConfig()

    # 1. YAML overrides dataclass defaults (yaml_item may be None)
    if yaml_item is not None:
        if not isinstance(yaml_item, dict):
            raise TypeError("Each YAML entry must be a mapping/dict")

        valid_keys = set(asdict(cfg).keys())
        unknown_keys = set(yaml_item.keys()) - valid_keys

        if unknown_keys:
            raise ValueError(f"Unknown keys in YAML config entry: {sorted(unknown_keys)}")

        for key, value in yaml_item.items():
            if key in {"coco_json", "output_jsonl"} and value is not None:
                value = Path(value)

            if key == "include_categories":
                value = normalize_include_categories(value)

            setattr(cfg, key, value)

    # 2. CLI overrides YAML only when explicitly provided
    cli_overrides = {
        "coco_json": args.coco_json,
        "output_jsonl": args.output_jsonl,
        "dataset_id": args.dataset_id,
        "split": args.split,
        "description": args.description,
        "annotation_source": args.annotation_source,
        "prompt_strategy": args.prompt_strategy,
        "target_strategy": args.target_strategy,
        "prompt_text": args.prompt_text,
        "disable_pbar": args.disable_pbar,
        "drop_empty": args.drop_empty,
        "include_categories": args.include_categories,
        "skip_invalid_bboxes": args.skip_invalid_bboxes,
        "include_instance_metadata": args.include_instance_metadata,
    }

    for key, value in cli_overrides.items():
        if value is None:
            continue

        if key in {"coco_json", "output_jsonl"}:
            value = Path(value)

        if key == "include_categories":
            value = normalize_include_categories(value)

        setattr(cfg, key, value)

    # 3. Validate required values
    missing = []

    if cfg.coco_json is None:
        missing.append("coco_json")

    if cfg.output_jsonl is None:
        missing.append("output_jsonl")

    if cfg.dataset_id is None:
        missing.append("dataset_id")

    if missing:
        raise ValueError(
            "Missing required config values after merging YAML and CLI: "
            f"{missing}. Provide them in YAML or through CLI."
        )

    return cfg

def build_configs_from_yaml_and_cli(args: argparse.Namespace) -> list[CocoToCanonicalConfig]:
    """
    Load the YAML (if provided) and return a list of CocoToCanonicalConfig objects.
    If the YAML root is a list, create one config per entry. If YAML root is a mapping,
    create a single config from it. If no YAML provided, create a single config using CLI only.
    """
    yaml_root = None
    if args.config is not None:
        yaml_root = load_yaml_config(args.config)

    configs: list[CocoToCanonicalConfig] = []

    if isinstance(yaml_root, list):
        # Each item in list should be a mapping
        for idx, item in enumerate(yaml_root):
            if item is None:
                item = {}
            cfg = build_config_from_yaml_and_cli(args, yaml_item=item)
            configs.append(cfg)
    else:
        # yaml_root may be dict or None
        cfg = build_config_from_yaml_and_cli(args, yaml_item=(yaml_root if isinstance(yaml_root, dict) else None))
        configs.append(cfg)

    return configs


# =============================================================================
# Geometry utilities
# =============================================================================

def clamp(v: float, lo: float, hi: float) -> float:
    return max(lo, min(hi, v))


def coco_xywh_to_clipped_xyxy(
    bbox_xywh: list[float],
    *,
    img_width: int,
    img_height: int,
) -> list[float]:
    """
    COCO bbox format:
        [x, y, width, height]

    Internal bbox format:
        [x1, y1, x2, y2]
    """
    x, y, w, h = bbox_xywh

    x1 = clamp(float(x), 0.0, float(img_width))
    y1 = clamp(float(y), 0.0, float(img_height))
    x2 = clamp(float(x + w), 0.0, float(img_width))
    y2 = clamp(float(y + h), 0.0, float(img_height))

    return [x1, y1, x2, y2]


def is_valid_xyxy(box: list[float] | list[int]) -> bool:
    x1, y1, x2, y2 = box
    return x1 < x2 and y1 < y2


def normalize_xyxy(
    x1: float,
    y1: float,
    x2: float,
    y2: float,
    *,
    img_width: int,
    img_height: int,
) -> list[int]:
    """
    Normalize pixel xyxy box into canonical NORM_SIZExNORM_SIZE coordinate space.

    Convention:
            scale_x = NORM_SIZE / img_width
            scale_y = NORM_SIZE / img_height
    """
    scale_x = NORM_SIZE / img_width
    scale_y = NORM_SIZE / img_height

    norm = [
        round(x1 * scale_x),
        round(y1 * scale_y),
        round(x2 * scale_x),
        round(y2 * scale_y),
    ]

    return [
        int(clamp(norm[0], 0, NORM_SIZE)),
        int(clamp(norm[1], 0, NORM_SIZE)),
        int(clamp(norm[2], 0, NORM_SIZE)),
        int(clamp(norm[3], 0, NORM_SIZE)),
    ]


def norm_xyxy_to_canonical_bbox(box: list[int]) -> dict[str, dict[str, int]]:
    x1, y1, x2, y2 = box

    if x1 >= x2 or y1 >= y2:
        raise ValueError(f"Invalid normalized bbox: {box}")

    return {
        "tl": {"x": x1, "y": y1},
        "br": {"x": x2, "y": y2},
    }


def bbox_center_point_from_norm_xyxy(box: list[int]) -> dict[str, int]:
    x1, y1, x2, y2 = box

    return {
        "x": round((x1 + x2) / 2),
        "y": round((y1 + y2) / 2),
    }


