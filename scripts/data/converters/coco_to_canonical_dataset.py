def convert_coco_annotation_to_canonical_instance(
    ann: dict[str, Any],
    *,
    img_width: int,
    img_height: int,
    id_to_name: dict[int, str],
    cfg: CocoToCanonicalConfig,
) -> tuple[dict[str, Any], dict[str, Any], dict[str, Any] | None] | None:
    """
    Converts one COCO annotation into:

    1. canonical Annotation-compatible dict for Target.instances
    2. compact target item for Target.text
    3. optional metadata stored at sample level
    """
    category_id = int(ann["category_id"])
    class_name = id_to_name.get(category_id, str(category_id))

    xyxy = coco_xywh_to_clipped_xyxy(
        ann["bbox"],
        img_width=img_width,
        img_height=img_height,
    )

    if not is_valid_xyxy(xyxy):
        if cfg.skip_invalid_bboxes:
            return None

        raise ValueError(
            f"Invalid COCO bbox after clipping. "
            f"ann_id={ann.get('id')} box={xyxy}"
        )

    norm_box = normalize_xyxy(
        xyxy[0],
        xyxy[1],
        xyxy[2],
        xyxy[3],
        img_width=img_width,
        img_height=img_height,
    )

    if not is_valid_xyxy(norm_box):
        if cfg.skip_invalid_bboxes:
            return None

        raise ValueError(
            f"Invalid normalized bbox. ann_id={ann.get('id')} "
            f"pixel_box={xyxy} norm_box={norm_box}"
        )

    canonical_instance: dict[str, Any] = {
        # New canonical_schema: label must always be int.
        "label": category_id,

        # Bounding box in canonical normalized 1000x1000 coordinates.
        "bbox": norm_xyxy_to_canonical_bbox(norm_box),

        # New canonical_schema: one positive point generated as bbox center.
        "pos_points": [
            bbox_center_point_from_norm_xyxy(norm_box)
        ],

        # No negative points generated by this converter.
        "neg_points": None,

        # COCO segmentation/RLE is not exported by this converter version.
        "mask": None,

        # Annotation source, e.g. "human", "pseudo_label", "ai_assisted".
        "source": cfg.annotation_source,
    }

    target_item = {
        "class": class_name,
        "box": norm_box,
    }

    instance_metadata = None

    if cfg.include_instance_metadata:
        instance_metadata = {
            "coco_annotation_id": ann.get("id"),
            "coco_category_id": category_id,
            "class_name": class_name,
            "coco_bbox_xywh": ann.get("bbox"),
            "area": ann.get("area"),
            "iscrowd": ann.get("iscrowd"),
            "attributes": ann.get("attributes"),
        }

    return canonical_instance, target_item, instance_metadata


def convert_coco_image_to_sample(
    image: dict[str, Any],
    anns: list[dict[str, Any]],
    *,
    sample_id: int,
    dataset_id: str,
    id_to_name: dict[int, str],
    prompt: str,
    cfg: CocoToCanonicalConfig,
    source_file: str,
) -> tuple[dict[str, Any], int]:
    img_width = int(image["width"])
    img_height = int(image["height"])
    image_id = int(image["id"])

    canonical_instances: list[dict[str, Any]] = []
    target_items: list[dict[str, Any]] = []
    instances_metadata: list[dict[str, Any]] = []
    skipped_invalid = 0

    for ann in anns:
        converted = convert_coco_annotation_to_canonical_instance(
            ann,
            img_width=img_width,
            img_height=img_height,
            id_to_name=id_to_name,
            cfg=cfg,
        )

        if converted is None:
            skipped_invalid += 1
            continue

        canonical_inst, target_item, instance_metadata = converted

        canonical_instances.append(canonical_inst)
        target_items.append(target_item)

        if instance_metadata is not None:
            instances_metadata.append(instance_metadata)

    target_text = build_target_text(cfg=cfg, target_items=target_items)

    sample = {
        "record_type": "sample",
        "sample_id": sample_id,
        "dataset_id": dataset_id,

        # New canonical_schema: query image is inside images dict.
        "images": {
            "query": {
                "path": image["file_name"],
                "size": [img_width, img_height],
            }
        },

        "messages": [
            {
                "role": "user",
                "content": [
                    {
                        "type": "image",
                        "image_id": "query",
                    },
                    {
                        "type": "text",
                        "text": prompt,
                    },
                ],
            }
        ],

        "target": {
            "text": target_text,
            "instances": canonical_instances,
        },

        "metadata": {
            "source_file": source_file,
            "annotation_source": cfg.annotation_source,
            "split": cfg.split,
            "coco_image_id": image_id,
            "original_file_name": image.get("file_name"),
            "num_instances": len(canonical_instances),
            "num_invalid_annotations_skipped": skipped_invalid,
            "target_format": cfg.target_strategy,
            "bbox_source": "coco_bbox_xywh",
            "pos_points_source": "bbox_center_generated",
            "neg_points_source": None,
            "mask_source": None,
        },
    }

    if instances_metadata:
        sample["metadata"]["instances_metadata"] = instances_metadata

    return sample, skipped_invalid


