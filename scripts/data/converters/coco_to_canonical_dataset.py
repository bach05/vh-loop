def build_dataset_info_record(
    *,
    cfg: CocoToCanonicalConfig,
    coco: dict[str, Any],
    id_to_name: dict[int, str],
    selected_category_ids: list[int],
) -> dict[str, Any]:
    assert cfg.dataset_id is not None

    label_info = {
        str(cat_id): id_to_name[cat_id]
        for cat_id in sorted(id_to_name.keys())
    }

    selected_label_info = {
        str(cat_id): id_to_name[cat_id]
        for cat_id in selected_category_ids
    }

    coco_info = coco.get("info", {}) or {}

    info = {
        "dataset_id": cfg.dataset_id,
        "description": cfg.description or coco_info.get("description") or "",
        "annotation_source": cfg.annotation_source,
        "split": cfg.split,
        "source_format": "COCO",
        "schema_version": SCHEMA_VERSION,
        "date_converted": datetime.now().isoformat(timespec="seconds"),

        "has_semantic_masks": False,
        "has_instance_masks": False,
        "has_point": True,
        "has_bbox": True,

        "label_info": label_info,
        "selected_label_info": selected_label_info,

        "prompt_strategy": cfg.prompt_strategy,
        "target_strategy": cfg.target_strategy,
        "target_coordinates": "normalized_1000_square",
        "bbox_source": "coco_bbox_xywh",
        "pos_points_source": "bbox_center_generated",
        "neg_points_source": None,
        "mask_source": None,

        "notes": (
            "Converted from COCO instance annotations. "
            "Bounding boxes are normalized to the canonical 1000x1000 coordinate space. "
            "Instance masks are not exported by this converter version. "
            "Positive points are generated as bbox centers. "
            "Negative points are not generated."
        ),
    }

    return {
        "record_type": "dataset_info",
        "schema_version": SCHEMA_VERSION,
        "info": info,
    }


# =============================================================================
# JSONL writer
